    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $messages = Message::with('from')->where('to', Auth::user()->id)->orderBy('isRead')->get();
        $message = Message::findOrFail($id);
        if( $message ) {
            if(!$message->isRead) {
                $message->isRead = true;
                $message->save();
            }
            return view('messages.messages', compact(['messages', 'message']));
        } else {
            abort('404');
        }
    }

