    {
        $num = Input::get('num', 8);
        $topics = Topic::latest()->limit($num)->get();

        foreach($topics as $topic) {
            $topic->views = $topic->views();
            $topic->likes = $topic->likes();
            $topic->lessons = $topic->lessonCount();
        }

        return $topics;
    }

